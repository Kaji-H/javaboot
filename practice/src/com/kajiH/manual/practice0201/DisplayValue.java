package com.kajiH.manual.practice0201;

public class DisplayValue {

	public static void main(String[] args) {
		// 父 ４２、母 ３８，ぼく ２０、僕の体重 640
		int fatherAge = 42;
		int motherAge = 38;
		int myAge = 20;
		int myWeight = 640;

		// 年齢とぼくの体重を表示
		System.out.println("僕のパパの年齢は" + fatherAge + "歳です。");
		System.out.println("僕のママの年齢は" + motherAge + "歳です。");
		System.out.println("僕の年齢は" + myAge + "歳です。");
		System.out.println();
		System.out.println("僕の体重は" + myWeight + "kg です。");

	}

}
