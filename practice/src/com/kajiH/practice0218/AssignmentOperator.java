package com.kajiH.practice0218;

public class AssignmentOperator {
    public static void main(String[] args) {
        int a = 4;
        int target;

        target = a;
        System.out.println("target = a:" + target);

        target += a;
        System.out.println("target += a:" + target);

        target *= a;
        System.out.println("target *= a:" + target);
    }
}
